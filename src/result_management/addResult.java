    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String networking = jTextField4.getText();
        String DBMS = jTextField5.getText();
        String OS = jTextField6.getText();

        //Establishing connection with mysql
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms" , "root" , "root");
            //We need to check if rollNo exists in database. To receive a query from sql, we will use resultSet
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollNo='"+rollNo+"'");
            
            //rs will actually now point to the 0th row. To check if data exists, we need to move 1 row forward, which can be
            //done using .next()
            if(rs.next()){
                st.executeUpdate("insert into result (rollNo , physics, maths, networking , DBMS , OS) values ('"+rollNo+"','"+physics+"','"+maths+"','"+networking+"','"+DBMS+"','"+OS+"')");
                JOptionPane.showMessageDialog(null, "Result updated successfully");
                setVisible(false);
                new addResult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Entered Roll Number does not exist");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

