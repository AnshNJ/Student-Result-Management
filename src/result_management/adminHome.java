    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String gender = (String)jComboBox3.getSelectedItem();
        String rollNo = jTextField1.getText();
        String name = jTextField2.getText();
        String guardianName = jTextField3.getText();
        
        if(name.length() < 1 || guardianName.length() < 1 || rollNo.length() < 1){
            JOptionPane.showMessageDialog(null, "Please fill all the necessary fields");
        } else {
            //Establishing connection with Database
            try{
                //Call Mysql driver class
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root" , "root");

                //Handling sql commands
                Statement st = con.createStatement();
                st.executeUpdate("insert into student (rollNo , course , branch , name , gender , guardianName) values ('"+rollNo+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+guardianName+"' )");
                
                JOptionPane.showMessageDialog(null, "Student added successfully");
                setVisible(false);
                new adminHome().setVisible(true);
            } catch(Exception e){
                JOptionPane.showMessageDialog(null , e.toString());
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

